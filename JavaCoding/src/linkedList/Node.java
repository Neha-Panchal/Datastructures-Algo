package linkedList;

public class Node{

	int reference;
	Node data;
	
	public int getReference() {
		return reference;
	}
	public void setReference(int reference) {
		this.reference = reference;
	}
	public Node getData() {
		return data;
	}
	public void setData(Node data) {
		this.data = data;
	}
	
}
